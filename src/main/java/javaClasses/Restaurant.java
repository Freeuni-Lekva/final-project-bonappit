package javaClasses;

public class Restaurant {
    String name;
    String password;
    String id;
    String menu;
    int numTable;
    double rating;
    public Restaurant(String name,String password,String id,String menu,int numTable,double rating){
        this.name=name;
        this.password=password;
        this.id=id;
        this.menu=menu;
        this.numTable=numTable;
        this.rating=rating;
    }


}
